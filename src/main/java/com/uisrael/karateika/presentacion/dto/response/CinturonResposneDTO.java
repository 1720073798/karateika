package com.uisrael.karateika.presentacion.dto.response;

public class CinturonResposneDTO {
	
	private int cin_id;
	private String cin_nombre;
	private String cin_nivel;
	private String cin_descripcion;
	
	public int getCin_id() {
		return cin_id;
	}
	public void setCin_id(int cin_id) {
		this.cin_id = cin_id;
	}
	public String getCin_nombre() {
		return cin_nombre;
	}
	public void setCin_nombre(String cin_nombre) {
		this.cin_nombre = cin_nombre;
	}
	public String getCin_nivel() {
		return cin_nivel;
	}
	public void setCin_nivel(String cin_nivel) {
		this.cin_nivel = cin_nivel;
	}
	public String getCin_descripcion() {
		return cin_descripcion;
	}
	public void setCin_descripcion(String cin_descripcion) {
		this.cin_descripcion = cin_descripcion;
	}
}
